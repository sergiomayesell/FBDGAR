    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection connection;
        String host = jTextField3.getText();
        String port = jTextField4.getText();
        String db = jTextField5.getText();
        String pass = new String(jPasswordField1.getPassword());
        String url = "jdbc:postgresql://" + host + ":" + port + "/" + db;
        String toF = url + "-.-" + pass;
        try {
            Class.forName("org.postgresql.Driver");
            connection = DriverManager.getConnection(url, "postgres", pass);
            if (connection != null) {
                Crypter ct = new Crypter();
                byte[] textoCifrado = ct.encrypt("s8a5*.7s7#a5sd1*".getBytes(), toF);
                //File f = new File("dbInfo.cfg");
                Files.write(Paths.get("dbInfo.cfg"), textoCifrado);
                UserInterface uI = new UserInterface(connection);
                uI.setVisible(true);
                dispose();
                //System.out.println("Concetando a Base de Datos...");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Datos incorrectos", "DB conection failed", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

